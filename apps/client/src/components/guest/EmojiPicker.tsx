interface EmojiPickerProps {
  isOpen: boolean;
  onClose: () => void;
  onSelect: (emoji: string) => void;
}

export default function EmojiPicker({ isOpen, onClose, onSelect }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState('smileys');
  const pickerRef = useRef<HTMLDivElement>(null);

  // Close on outside click
  useEffect(() => {
    if (!isOpen) return;
    const handleClick = (e: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(e.target as Node)) {
        onClose();
      }
    };
    document.addEventListener('mousedown', handleClick);
    return () => document.removeEventListener('mousedown', handleClick);
  }, [isOpen, onClose]);

  const activeEmojis = CATEGORIES.find(c => c.key === activeCategory)?.emojis || [];

  return (
    <AnimatePresence>
      {isOpen && (
        <motion.div
          ref={pickerRef}
          initial={{ opacity: 0, y: 10, scale: 0.95 }}
          animate={{ opacity: 1, y: 0, scale: 1 }}
          exit={{ opacity: 0, y: 10, scale: 0.95 }}
          transition={{ type: 'spring', stiffness: 400, damping: 28 }}
          className="absolute bottom-full left-0 right-0 mb-2 mx-2 rounded-2xl border border-glass-border bg-obsidian/95 backdrop-blur-xl shadow-2xl z-50 overflow-hidden"
        >
          {/* Category tabs */}
          <div className="flex gap-0.5 px-2 pt-2 pb-1 border-b border-glass-border overflow-x-auto scrollbar-hide">
            {CATEGORIES.map(cat => (
              <button
                key={cat.key}
                onClick={() => setActiveCategory(cat.key)}
                className={cn(
                  "px-2.5 py-1.5 rounded-lg text-base transition-colors shrink-0 cursor-pointer",
                  activeCategory === cat.key
                    ? "bg-gold/15 ring-1 ring-gold/30"
                    : "hover:bg-white/[0.04]"
                )}
              >
                {cat.label}
              </button>
            ))}
          </div>

          {/* Emoji grid */}
          <div className="grid grid-cols-8 gap-0.5 p-2 max-h-[200px] overflow-y-auto scrollbar-thin">
            {activeEmojis.map((emoji, i) => (
              <button
                key={`${emoji}-${i}`}
                onClick={() => onSelect(emoji)}
                className="w-full aspect-square flex items-center justify-center text-xl rounded-lg hover:bg-white/[0.06] active:scale-90 transition-all cursor-pointer"
              >
                {emoji}
              </button>
            ))}
          </div>
        </motion.div>
      )}
    </AnimatePresence>
  );
}
